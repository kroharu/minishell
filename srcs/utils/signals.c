#include "minishell.h"

void	sigquit_handler_child(int signum)
{
	if (signum == SIGQUIT)
		exit(131);
}

void	sigint_empty_handler(int signum)
{
	if (signum == SIGINT)
	{
		printf("%s \b\b    \n", PROMPT);
		rl_on_new_line();
		rl_replace_line("", 0);
		rl_redisplay();
	}
}

void	sigint_handler_parent(int signum)
{
	(void)signum;
}

void	sigquit_handler_parent(int signum)
{
	t_env	*tmp;

	if (signum == SIGQUIT)
	{
		signal(SIGQUIT, SIG_IGN);
		kill(0, SIGQUIT);
		tmp = find_env(g_info->env_list, "_");
		if (ft_strcmp(tmp->value, "./minishell", -1))
			write(1, "Quit: 3\n", 9);
	}
}
